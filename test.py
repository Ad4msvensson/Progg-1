def maximum(a, b):
    # Funktionen maximum returnerar det största talet.
    return max(a, b)

def addera(a, b):
    # Funktionen addera lägger ihop talen och returnerar svaret.
    return a + b

def hej(namn):
    # Funktionen hej skriver ut en hälsningsfras med det givna namnet.
    print('hej {namn}')