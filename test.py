def maximum(a, b):
    if a > b:
        return a
    else:
        return b


print(maximum(3, 7))
# Funktionen maximum returnerar det största talet.
# utskrift: 7

print(maximum(11, 3))
# utskrift: 11

def addera(a, b):
    return a + b


print(addera(3, 5))
# Funktionen addera lägger ihop talen och returnerar svaret.
# utskrift 8

print(addera(10, 2))
